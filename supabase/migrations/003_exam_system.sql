    attempt_id IN (
      SELECT ea.id FROM exam_attempt ea
      JOIN student s ON s.id = ea.student_id
      WHERE s.email = public.get_auth_email()
    )
  );

CREATE POLICY "exam_attempt_answer_update_own"
  ON public.exam_attempt_answer FOR UPDATE
  USING (
    attempt_id IN (
      SELECT ea.id FROM exam_attempt ea
      JOIN student s ON s.id = ea.student_id
      WHERE s.email = public.get_auth_email()
    )
  );

-- ============================================
-- exam_event_log: Anti-cheat event tracking
-- ============================================
CREATE TABLE IF NOT EXISTS public.exam_event_log (
  id bigint generated by default as identity not null,
  attempt_id bigint not null references public.exam_attempt(id) on delete cascade,
  event_type text not null check (event_type in ('tab_switch', 'fullscreen_exit', 'copy_paste', 'right_click', 'warning_shown', 'force_submit')),
  severity text not null default 'warning' check (severity in ('info', 'warning', 'critical')),
  details text null,
  created_at timestamptz not null default now(),
  constraint exam_event_log_pkey primary key (id)
) tablespace pg_default;

